/**
 * Application-wide limits for recording and automation
 */

/**
 * Maximum number of actions that can be recorded in a single recording session
 * When this limit is reached, recording will automatically stop
 */
export const MAX_RECORDING_ACTIONS = 150;

/**
 * Maximum number of execution steps allowed in a single automation session
 * When this limit is reached, automation will automatically stop
 */
export const MAX_AUTOMATION_STEPS = 150;
